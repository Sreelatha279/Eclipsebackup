package Loops;

public class Forloop {

	public static void main(String[] args) 
	{
		//print odd numbers
		for(int i=1;i<=10;i+=2)
			System.out.println(i);
		
		//print even numbers
				for(int i=0;i<=10;i+=2)
					System.out.println(i);
				
		//print even numbers in reverse
				for(int i=10;i>=0;i-=2)
					System.out.println(i);

		//print odd numbers in reverse
				for(int i=9;i>=0;i-=2)
					System.out.println(i);
	}

}
